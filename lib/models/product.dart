class Product {
  final int id;
  final String title;
  final String description;
  final String category;
  final int stock;
  final String thumbnail;
  final double price;

  Product({
    required this.id,
    required this.title,
    required this.description,
    required this.category,
    required this.stock,
    required this.thumbnail,
    required this.price,
  });
}
